"""
COCO 17 키포인트 골격 토폴로지 정의.
TSSI 순서, 좌우 매핑, 부모 관절 매핑을 한 파일에 모아둠.

COCO 17 인덱스
   0: 코         5: 왼어깨    11: 왼골반     15: 왼발목
   1: 왼눈       6: 오른어깨   12: 오른골반    16: 오른발목
   2: 오른눈     7: 왼팔꿈치    13: 왼무릎
   3: 왼귀       8: 오른팔꿈치  14: 오른무릎
   4: 오른귀     9: 왼손목
              10: 오른손목
"""

NUM_KEYPOINTS = 17

# ---------------------------------------------------------------------------
# TSSI (Tree-Structured Skeleton Image) 순서
# 인체 트리 구조를 DFS 로 순회하면서 관절을 일렬로 나열.
# 가로축 인접성이 실제 인접성과 일치하도록 함.
#
# 경로:
#   왼발목→왼무릎→왼골반 →왼어깨→ 왼팔꿈치→왼손목→ (복귀)
#   →왼어깨→코→ 왼눈→왼귀→ (복귀) →코→
#   오른눈→오른귀→(복귀)→코→오른어깨→
#   오른팔꿈치→오른손목→(복귀)→오른어깨→
#   오른골반→오른무릎→오른발목
# ---------------------------------------------------------------------------
TSSI_ORDER = [
    15, 13, 11,         # 왼발목→왼무릎→왼골반
    5,                  # 왼어깨
    7, 9,               # 왼팔꿈치→왼손목
    7, 5,               # 복귀
    0,                  # 코
    1, 3, 1,            # 왼눈→왼귀→복귀
    0,                  # 코
    2, 4, 2,            # 오른눈→오른귀→복귀
    0,                  # 코
    6,                  # 오른어깨
    8, 10,              # 오른팔꿈치→오른손목
    8, 6,               # 복귀
    12,                 # 오른골반
    14, 16,             # 오른무릎→오른발목
]
TSSI_LEN = len(TSSI_ORDER)  # = 25


# ---------------------------------------------------------------------------
# 좌우 반전 매핑 (augmentation 시 좌우 키포인트 인덱스 swap)
# COCO 17 기준. naive 순서 적용.
# ---------------------------------------------------------------------------
COCO_FLIP_PAIRS = [
    (1, 2),   # 눈
    (3, 4),   # 귀
    (5, 6),   # 어깨
    (7, 8),   # 팔꿈치
    (9, 10),  # 손목
    (11, 12), # 골반
    (13, 14), # 무릎
    (15, 16), # 발목
]


# ---------------------------------------------------------------------------
# 부모 관절 매핑 (bone direction = current joint - parent joint).
# Root = -1 (부모 없음). 부모로부터의 dx, dy 를 'angle' 채널로 사용.
# 의도: 각 뼈의 방향(2D 벡터)이 자세 정보를 담음.
#       회전 불변성은 아니지만, 절대 좌표가 아닌 상대 좌표라 정규화에 유리.
# ---------------------------------------------------------------------------
COCO_PARENT = {
    0: -1,     # 코 (루트)
    1: 0,      # 왼눈    ← 코
    2: 0,      # 오른눈  ← 코
    3: 1,      # 왼귀    ← 왼눈
    4: 2,      # 오른귀  ← 오른눈
    5: 0,      # 왼어깨  ← 코 (목 대용)
    6: 0,      # 오른어깨 ← 코
    7: 5,      # 왼팔꿈치 ← 왼어깨
    8: 6,      # 오른팔꿈치← 오른어깨
    9: 7,      # 왼손목  ← 왼팔꿈치
    10: 8,     # 오른손목← 오른팔꿈치
    11: 5,     # 왼골반  ← 왼어깨
    12: 6,     # 오른골반← 오른어깨
    13: 11,    # 왼무릎  ← 왼골반
    14: 12,    # 오른무릎← 오른골반
    15: 13,    # 왼발목  ← 왼무릎
    16: 14,    # 오른발목← 오른무릎
}


def get_order(name):
    """순서 이름 → 인덱스 리스트"""
    if name == 'naive':
        return list(range(NUM_KEYPOINTS))     # [0, 1, ..., 16]
    if name == 'tssi':
        return list(TSSI_ORDER)
    raise ValueError(f"Unknown order: {name!r}. Use 'naive' or 'tssi'.")
